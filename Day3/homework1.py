'''
    WAP in Python to accept amount from user
    and print number of possible notes
'''

A=int(input("Enter the amount"))
print("Rs 10:",A//10)
print("Rs 20:",A//20)
print("Rs 50:",A//50)
print("Rs 100:",A//100)
print("Rs 200:",A//200)
print("Rs 500:",A//500)
print("Rs 2000:",A//2000)
